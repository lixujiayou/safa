public class IntentLatLngBean {
    private String alat;
    private String alng;
    private String zlat;
    private String zlng;

    public String getAlat() {
        return alat;
    }

    public void setAlat(String alat) {
        this.alat = alat;
    }

    public String getAlng() {
        return alng;
    }

    public void setAlng(String alng) {
        this.alng = alng;
    }

    public String getZlat() {
        return zlat;
    }

    public void setZlat(String zlat) {
        this.zlat = zlat;
    }

    public String getZlng() {
        return zlng;
    }

    public void setZlng(String zlng) {
        this.zlng = zlng;
    }
}
